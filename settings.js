function importData(event) {
  const file = event.target.files?.[0];
  if (!file) return;
  const reader = new FileReader();
  reader.onload = () => {
    try {
      const text = reader.result;
      if (file.name.endsWith('.csv')) {
        parseCsvImport(text);
      } else {
        const data = JSON.parse(text);
        if (data.type === 'scores' && data.scores) {
          state.scores = data.scores;
          localStorage.setItem('impostor-scores', JSON.stringify(state.scores));
          renderScores();
          alert('Scores imported successfully.');
        } else if (data.type === 'wordpack' && typeof data.custom === 'string') {
          state.settings.customWordpackText = data.custom;
          customWordEntries = parseCustomWordPack(data.custom);
          state.settings.useCustomWords = true;
          persistSettings();
          renderCategoryOptions();
          renderCustomWordPackInfo();
          alert('Word pack imported successfully.');
        } else {
          throw new Error('Unsupported import format.');
        }
      }
    } catch (error) {
      alert('Unable to import file.');
    }
  };
  reader.readAsText(file);
}

function parseCsvImport(text) {
  const lines = text.split('\n').map((line) => line.trim()).filter(Boolean);
  const entries = [];
  lines.forEach((line) => {
    const [word, hint] = line.split(',').map((part) => part.trim());
    if (word && hint) entries.push(`${word} - ${hint}`);
  });
  if (entries.length) {
    state.settings.customWordpackText = entries.join('\n');
    customWordEntries = parseCustomWordPack(state.settings.customWordpackText);
    state.settings.useCustomWords = true;
    persistSettings();
    renderCategoryOptions();
    renderCustomWordPackInfo();
    alert(`${entries.length} custom word${entries.length !== 1 ? 's' : ''} imported.`);
  } else {
    alert('No valid CSV entries found.');
  }
}

